package com.destination.septemberbatch.sep20;

public class loops {

	public static void main(String[] args) 
	{
		System.out.println("-----FOR loop-----");
				for(int i=1;i<=10;i++)
				{
					System.out.println("hi");
				}
				System.out.println("----------------------------------");
			System.out.println("---------While loop---------");
			int j=1;
			while(j<=10)
			{
				System.out.println("Hi");
				j++;
			}
			System.out.println("-----------------------");
			System.out.println("------do while--------");
			int k=1;
			do
			{
				System.out.println("HI");
				k++;
			}
			while(k<=-10);
	}

}
