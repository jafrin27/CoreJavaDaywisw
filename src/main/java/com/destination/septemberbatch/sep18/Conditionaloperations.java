package com.destination.septemberbatch.sep18;

public class Conditionaloperations 
{
	public static void main(String[] args) 
	{
		System.out.println("Conditional operations");
		System.out.println("===================");
		int a = 100;
		int b = 50;
		System.out.println("value No :1="+a);
		System.out.println("value No :2="+b);
		System.out.println("===================");
		boolean res1=a>b;
		System.out.println("Greater than result = "+res1);
		System.out.println("===================");
		boolean res2=a<b;
		System.out.println("Lesser than Equals result = "+res2);
		System.out.println("===================");
		boolean res3=a>=b;
		System.out.println("Greater than equals result = "+res3);
		System.out.println("===================");
		boolean res4=a<=b;
		System.out.println("Lesser than result = "+res4);
		System.out.println("===================");
		boolean res5=a==b;
		System.out.println("Equals result = "+res5);
		System.out.println("===================");
		boolean res6=a!=b;
		System.out.println("Not equals result = "+res6);
		System.out.println("===================");
		

	}

}
